        <h3 class="first_topic">Descripción</h3> 
        
        <p>De conformidad a lo dispuesto en la Constitución Política de la República del Estado, artículo 19 Nº 4 y la Ley Nº 19.628 sobre protección de la vida privada o protección de datos de carácter personal y sus modificaciones posteriores, se señala lo siguiente:</p>
 
        <p>El Ministerio Secretaría General de la Presidencia, en adelante MINSEGPRES, pone en conocimiento de todos quienes acceden al sitio web <a href="http://alpha.chileclic.cl">http://alpha.chileclic.cl</a> la siguiente Política de Privacidad, a fin de resguardar la seguridad, confidencialidad y privacidad de los suscriptores, usuarios y/o visitantes de este sitio web.</p>
 
        <p>Estas políticas tienen por finalidad asegurar la correcta utilización de la información recopilada a través de las visitas del sitio web señalado y de los contenidos de su portal.</p>
 
        <h3>EN CUANTO A LA RECOPILACION DE DATOS</h3>
 
        <p>MINSEGPRES, a través del sitio web mencionado, recopila datos de los suscriptores, usuarios y/o visitantes que hagan uso del mismo, a través de dos mecanismos:</p>
 
        <p>i) Mecanismos Automáticos: Son aquellos procesos informáticos realizados para generar registros de las actividades de los visitantes de sitios Web y cuyo objeto es establecer patrones de actividad, navegación y audiencia, que no implican la identificación personal de aquellos suscriptores, usuarios y/o visitantes que accedan a los servicios del portal <a href="http://alpha.chileclic.cl">http://alpha.chileclic.cl</a>.</p>
 
        <p>MINSEGPRES se reserva el derecho de usar dicha información general, a fin de establecer criterios que mejoren su contenido, en todo caso siempre disociados de la persona que dejó los datos en su navegación.</p>
 
        <p>ii) Mecanismos Manuales: Son requerimientos formales y expresos de información a los suscriptores, usuarios y/o visitantes del portal que implican la recolección de datos como nombre, apellidos, domicilio, correo electrónico, ocupación, etc. Se indicará claramente el fin para el cual está siendo solicitada.</p>
 
        <h3>EN CUANTO A LA ENTREGA DE INFORMACIÓN A TERCEROS</h3>
 
        <p>Respecto de la entrega de información recopilada por medio de los mecanismos automáticos antes señalados, y que no contengan identificación personal de los suscriptores, usuarios y/o visitantes de la página, esta podrá ser utilizada para informar a entidades públicas, gubernamentales o a terceros sobre patrones de actividad, navegación, audiencia y áreas más visitadas de la pagina Web. Cuando se trate de datos recopilados que contengan información personal de los suscriptores, usuarios y/o visitantes de la página, solo podrá entregarse en razón de un mandato legal o una orden emanada de los Tribunales de Justicia que así lo establezca.</p>
 
        <p>Al acceder al portal, el visitante tendrá derecho a revisar toda la información que esté disponible en él, solo pudiendo utilizarla para fines particulares y no comerciales. Sin perjuicio de lo anterior, esta institución no se hace responsable por la veracidad o exactitud de la información contenida en los enlaces a otros sitios Web o que haya sido entregada por terceros.</p>
 
        <h3>DECLARACIÓN</h3>
 
        <p>Los suscriptores, usuarios y/o visitantes del Sistema declaran conocer y aceptar la circunstancia relativa a que este servicio público podrá en cualquier momento modificar el todo o parte de las presentes condiciones de uso, conforme la legislación vigente o políticas del organismo.</p>
 
        <h3>NOTA FINAL</h3>
 
        <p>Se hace presente, que por esta Política de Privacidad, el Ministerio Secretaría General de la Presidencia no está suscribiendo contrato ni compromiso alguno que la obligue con sus lectores, si no que solo tiene una finalidad informativa.</p>

